  echo -e "\033[34mlog\033[0m -вывод логов из /var/log/anaconda/X.log"
  echo -e "\033[34mexit\033[0m - выход"
  echo -e "\033[34mhelp\033[0m - помощь"
  if [[ "$2" != "interactive" ]]
  then
    echo -e "\033[34minteractive\033[0m - интерактивный режим работы"
  fi
  echo -e "\033[33mКод ошибки -1\033[0m - отсутствует исполняемый файл"
  echo -e "\033[33mКод ошибки -2\033[0m - недостаточно прав для запуска исполняемого файла"
  echo -e "\033[33mКод ошибки -3\033[0m - ошибка в написании аргументов определенного параметра"
  echo -e "\033[33mКод ошибки -4\033[0m - математическая ошибка"
  echo -e "\033[33mКод ошибки -5\033[0m - отсутсвует директория"
}

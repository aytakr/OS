function help {
  if [[ $1 -ne 1 ]]
  then
    echo "Ошибка - неправильное количество аргументов. Параметр help не имеет дополнительных аргументов"
    argumentError
  fi
  echo -e "\033[31mПараметры запуска:"
  echo -e "\033[34mcalc\033[0m - калькулятор. Введите sum/sub/mul/div обозначающее операцию и два целых числа"
  echo -e "\033[34msearch\033[0m - рекурсивный поиск содержимого файлов в директории. Введите директорию и регулярное выражение - объект поиска"
  echo -e "\033[34mreverse\033[0m - запись в обратном порядке содержимого первого файла во второй файл. Введите названия двух файлов"
